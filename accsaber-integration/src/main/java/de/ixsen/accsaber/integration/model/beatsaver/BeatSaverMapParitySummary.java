package de.ixsen.accsaber.integration.model.beatsaver;

public class BeatSaverMapParitySummary {
    private int errors;

    private int resets;

    private int warns;

    public int getErrors() {
        return errors;
    }

    public void setErrors(int errors) {
        this.errors = errors;
    }

    public int getResets() {
        return resets;
    }

    public void setResets(int resets) {
        this.resets = resets;
    }

    public int getWarns() {
        return warns;
    }

    public void setWarns(int warns) {
        this.warns = warns;
    }
}
