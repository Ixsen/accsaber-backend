package de.ixsen.accsaber.integration.model.beatsaver;

public class BeatSaverExistingDifficulties {
    private boolean easy;

    private boolean expert;

    private boolean expertPlus;

    private boolean hard;

    private boolean normal;

    public void setEasy(boolean easy) {
        this.easy = easy;
    }

    public boolean getEasy() {
        return this.easy;
    }

    public void setExpert(boolean expert) {
        this.expert = expert;
    }

    public boolean getExpert() {
        return this.expert;
    }

    public void setExpertPlus(boolean expertPlus) {
        this.expertPlus = expertPlus;
    }

    public boolean getExpertPlus() {
        return this.expertPlus;
    }

    public void setHard(boolean hard) {
        this.hard = hard;
    }

    public boolean getHard() {
        return this.hard;
    }

    public void setNormal(boolean normal) {
        this.normal = normal;
    }

    public boolean getNormal() {
        return this.normal;
    }
}
