package de.ixsen.accsaber.integration.model.beatsaver;

public class BeatSaverUserDiffStats {
    private int easy;

    private int normal;

    private int hard;

    private int expert;

    private int expertPlus;

    private int total;

    public int getEasy() {
        return easy;
    }

    public void setEasy(int easy) {
        this.easy = easy;
    }

    public int getNormal() {
        return normal;
    }

    public void setNormal(int normal) {
        this.normal = normal;
    }

    public int getHard() {
        return hard;
    }

    public void setHard(int hard) {
        this.hard = hard;
    }

    public int getExpert() {
        return expert;
    }

    public void setExpert(int expert) {
        this.expert = expert;
    }

    public int getExpertPlus() {
        return expertPlus;
    }

    public void setExpertPlus(int expertPlus) {
        this.expertPlus = expertPlus;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
